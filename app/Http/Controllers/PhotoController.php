    public function getPhotos($category_id)
    {
        $categories = PhotoCategory::all();
        if ($category_id != null && is_numeric($category_id) && $categories->contains('id', $category_id)){
            $selected_category = $categories->firstWhere('id', $category_id);
        }
        else {
            $selected_category =  $categories->first();
        }
        $category_id = $selected_category->id;

        $photos = Photo::where('category_id', $category_id)->simplePaginate(6);

        return view('photo', compact('photos', 'selected_category', 'categories'));
    }

